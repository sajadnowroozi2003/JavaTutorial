class LogicalOP{
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        boolean c = false;
        boolean d = false;


        System.out.println("a && b: " + (a && b));


        // Logical OR
        System.out.println("a || b: " + (a || b || c || d));



        // Logical NOT
        System.out.println("!a: " + (!a));












    
    }
}