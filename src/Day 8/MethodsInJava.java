class MethodsInJava{

// Simple Method

        public static void printName(){
            System.out.println("Your name is sajad");
        }




// Method with  one Parameters

        public static void printName2(String name){
             System.out.println("Your name is "+name);
        }




// Multi-parameter method

        public static void  printName3(String name, String lname , int age, String phone){
            System.out.println("Hello "+name+" "+lname+".\nYou are "+age+" Years old.\n"+"your phone number is "+phone+".");
        }




// Example of Return Type Method return sum of two numbers.

        public static int  sumNumber(int num1 , int num2){
            int total = num1+num2;
            return total;
        }









}

