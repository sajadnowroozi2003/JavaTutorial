package Exam.Exam2018.Oppgava1;

public class Main {
    public static void main(String[] args) {


        System.out.println("=== With For ===");
        for (int i =1 ; i<=25 ; i ++){
            System.out.println(i);

        }


        System.out.println("=== With While ===");
        int a = 1;
        while (a<=25){

            System.out.println(a);
            a++;
        }
    }
}
